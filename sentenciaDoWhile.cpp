/*Sentencia Do While*/
#include<iostream>
#include<stdlib.h>
using namespace std;

int main(){
	int i;
	i = 10;
	
	do{
	cout<<i<<endl;
	i--; //aumenta el iterador de uno en uno	
		
	}while(i>=1);
	
	
	system("pause");
	return 0;
}
//Do While, orimero actua, luego piensa
//While, piensa y luego actua
